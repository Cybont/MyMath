using System;
using System.Collections.Generic;
using System.Text;

namespace MyMath
{
    public static class Math
    {
        public static int Add(int a, int b)
        {
            return a+b;
        }
        public static int Divide(int a, int b)
        {
            return a/b;
        }
        public static int Subtract(int a, int b)
        {
            return a-b;
        }
        public static int Multi(int a, int b)
        {
            return a*b;
        }
    }
}
